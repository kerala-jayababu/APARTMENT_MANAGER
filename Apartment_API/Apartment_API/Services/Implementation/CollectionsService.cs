    public async Task<(byte[] Content, string ContentType, string FileName)> ExportInvoicesAsync(
        int apartmentId, string format, string? search, string? period, int? incomeHeadId, int? statusId, CancellationToken cancellationToken = default)
    {
        var data = await ListInvoicesAsync(apartmentId, search, period, incomeHeadId, statusId, 1, 1000, cancellationToken).ConfigureAwait(false);
        var fmt = (format ?? string.Empty).Trim().ToLowerInvariant();
        if (fmt is not ("xlsx" or "pdf")) throw new InvalidOperationException("format must be xlsx or pdf.");
        var sb = new StringBuilder();
        sb.AppendLine("InvoiceNumber,UnitNumber,OwnerName,IncomeHead,InvoiceDate,DueDate,TotalAmount,PaidAmount,Balance,Status");
        foreach (var x in data.Items)
        {
            sb.AppendLine($"{Csv(x.InvoiceNumber)},{Csv(x.UnitNumber)},{Csv(x.OwnerName)},{Csv(x.IncomeHeadName)},{x.InvoiceDate:yyyy-MM-dd},{x.DueDate:yyyy-MM-dd},{x.TotalAmount},{x.PaidAmount},{x.BalanceAmount},{Csv(x.StatusCode)}");
        }
        var bytes = Encoding.UTF8.GetBytes(sb.ToString());
        var periodLabel = string.IsNullOrWhiteSpace(period) ? DateTime.UtcNow.ToString("MMMyyyy", CultureInfo.InvariantCulture) : period.Replace("-", "");
        if (fmt == "xlsx")
            return (bytes, "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet", $"Invoices_{periodLabel}.xlsx");
        return (bytes, "application/pdf", $"Invoices_{periodLabel}.pdf");
    }

