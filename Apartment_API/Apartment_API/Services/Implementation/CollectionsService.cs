    public async Task<SaveQuickPostReceiptsResponseDto> SaveQuickPostReceiptsAsync(
        int apartmentId, int userId, SaveQuickPostReceiptsRequest request, CancellationToken cancellationToken = default)
    {
        await EnsurePermissionAsync(apartmentId, PermissionType.Create, cancellationToken).ConfigureAwait(false);
        if (request.ApartmentId != apartmentId) throw new InvalidOperationException("Apartment mismatch.");
        if (request.Receipts.Count == 0) throw new InvalidOperationException("At least one receipt row is required.");
        if (request.Receipts.Count > 50) throw new InvalidOperationException("Maximum 50 rows allowed.");

        var modeCodes = await db.PaymentModes.AsNoTracking().Where(x => x.IsActive)
            .Select(x => x.PaymentModeCode.ToUpper()).ToListAsync(cancellationToken).ConfigureAwait(false);
        var statusPaidId = await ResolveInvoiceStatusIdAsync("PAID", cancellationToken).ConfigureAwait(false);
        var statusPartialId = await ResolveInvoiceStatusIdAsync("PARTIAL", cancellationToken).ConfigureAwait(false);

        var results = new List<QuickPostReceiptResultDto>(request.Receipts.Count);
        var now = DateTime.UtcNow;

        foreach (var row in request.Receipts)
        {
            var blank = row.PaidDate == null && row.AmountPaid == null && string.IsNullOrWhiteSpace(row.PaymentMode);
            if (blank) continue;
            try
            {
                ValidateQuickPostRow(row, modeCodes);
                var invoice = await db.Invoices.FirstOrDefaultAsync(
                    x => x.ApartmentId == apartmentId && x.IdInvoice == row.InvoiceId, cancellationToken).ConfigureAwait(false);
                if (invoice is null) throw new InvalidOperationException("INVOICE_NOT_FOUND");
                if (invoice.UnitId != row.UnitId || invoice.PersonId != row.PersonId)
                    throw new InvalidOperationException("Invoice, unit, and person mismatch.");

                var fiscalLocked = await db.FiscalYears.AsNoTracking()
                    .Where(x => x.ApartmentId == apartmentId && x.IdFiscalYear == invoice.FiscalYearId)
                    .Select(x => (bool?)x.IsLocked)
                    .FirstOrDefaultAsync(cancellationToken).ConfigureAwait(false);
                if (fiscalLocked == true) throw new InvalidOperationException("PERIOD_LOCKED");

                var outstanding = invoice.BalanceAmount ?? Math.Max(0m, invoice.TotalAmount - invoice.PaidAmount);
                if (row.AmountPaid!.Value <= 0 || row.AmountPaid.Value > outstanding)
                    throw new InvalidOperationException($"Amount paid ({row.AmountPaid.Value}) exceeds outstanding balance ({outstanding}).");

                await using var tx = await db.Database.BeginTransactionAsync(cancellationToken).ConfigureAwait(false);
                var receipt = new PaymentReceipt
                {
                    ApartmentId = apartmentId,
                    ReceiptNumber = BuildReceiptNumber(now),
                    ReceiptDate = row.PaidDate!.Value.Date,
                    UnitId = row.UnitId,
                    PersonId = row.PersonId,
                    PaymentMode = row.PaymentMode!.Trim().ToUpperInvariant(),
                    BankAccountId = row.BankAccountId,
                    ChequeNo = string.IsNullOrWhiteSpace(row.ChequeNo) ? null : row.ChequeNo.Trim(),
                    ChequeDate = row.ChequeDate?.Date,
                    ReferenceNo = string.IsNullOrWhiteSpace(row.ReferenceNo) ? null : row.ReferenceNo.Trim(),
                    TotalAmount = row.AmountPaid.Value,
                    AllocatedAmount = row.AmountPaid.Value,
                    UnallocatedAmount = 0m,
                    IsAdvance = false,
                    Status = "POSTED",
                    Narration = string.IsNullOrWhiteSpace(row.Remarks) ? null : row.Remarks.Trim(),
                    LockedInPeriodId = invoice.FiscalYearId,
                    CurrencyCode = "INR",
                    CreatedBy = userId,
                    CreatedAt = now
                };
                db.PaymentReceipts.Add(receipt);
                await db.SaveChangesAsync(cancellationToken).ConfigureAwait(false);

                db.InvoiceReceipts.Add(new InvoiceReceipt
                {
                    InvoiceId = invoice.IdInvoice,
                    PaymentReceiptId = receipt.IdPaymentReceipt,
                    AllocatedAmount = row.AmountPaid.Value,
                    AllocationDate = row.PaidDate.Value.Date,
                    Remarks = receipt.Narration,
                    CreatedBy = userId,
                    CreatedAt = now
                });

                invoice.PaidAmount += row.AmountPaid.Value;
                invoice.BalanceAmount = Math.Max(0m, invoice.TotalAmount - invoice.PaidAmount);
                var newStatus = invoice.BalanceAmount <= 0 ? "PAID" : "PARTIAL";
                invoice.StatusId = invoice.BalanceAmount <= 0 ? statusPaidId : statusPartialId;
                invoice.UpdatedAt = now;
                invoice.UpdatedBy = userId;

                await db.SaveChangesAsync(cancellationToken).ConfigureAwait(false);
                await tx.CommitAsync(cancellationToken).ConfigureAwait(false);

                results.Add(new QuickPostReceiptResultDto
                {
                    InvoiceId = row.InvoiceId,
                    ReceiptId = receipt.IdPaymentReceipt,
                    ReceiptNumber = receipt.ReceiptNumber,
                    AmountPaid = row.AmountPaid,
                    NewInvoiceStatus = newStatus,
                    JvReference = BuildJvReference(now),
                    Success = true
                });
            }
            catch (Exception ex)
            {
                results.Add(new QuickPostReceiptResultDto
                {
                    InvoiceId = row.InvoiceId,
                    AmountPaid = row.AmountPaid,
                    Success = false,
                    ErrorMessage = ex is InvalidOperationException ? ex.Message : "Unexpected error."
                });
            }
        }

        var saved = results.Count(x => x.Success);
        var errors = results.Count(x => !x.Success);
        if (saved == 0 && errors > 0)
            throw new InvalidOperationException("ALL_ROWS_INVALID");
        return new SaveQuickPostReceiptsResponseDto { SavedCount = saved, ErrorCount = errors, Results = results };
    }

