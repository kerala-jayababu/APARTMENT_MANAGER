    private IQueryable<InvoiceJoinRow> BaseInvoiceQuery(int apartmentId) =>
        from i in db.Invoices.AsNoTracking()
        where i.ApartmentId == apartmentId
        join u in db.Units.AsNoTracking() on i.UnitId equals u.IdUnit
        join p in db.Persons.AsNoTracking() on i.PersonId equals p.IdPerson
        join h in db.IncomeHeads.AsNoTracking() on i.IncomeHeadId equals h.IdIncomeHead
        join s in db.InvoiceStatuses.AsNoTracking() on i.StatusId equals s.IdInvoiceStatus
        select new InvoiceJoinRow
        {
            i = i,
            u = u,
            p = p,
            h = h,
            s = s
        };

    private static (DateTime Start, DateTime End, string Label) ResolvePeriod(string? period)
    {
        if (string.IsNullOrWhiteSpace(period))
        {
            var now = DateTime.UtcNow;
            var start = new DateTime(now.Year, now.Month, 1);
            return (start, start.AddMonths(1), now.ToString("MMM yyyy", CultureInfo.InvariantCulture));
        }

        var txt = period.Trim();
        if (DateTime.TryParseExact(txt, "MMM yyyy", CultureInfo.InvariantCulture, DateTimeStyles.None, out var d1))
        {
            var start = new DateTime(d1.Year, d1.Month, 1);
            return (start, start.AddMonths(1), d1.ToString("MMM yyyy", CultureInfo.InvariantCulture));
        }
        if (DateTime.TryParseExact(txt + "-01", "yyyy-MM-dd", CultureInfo.InvariantCulture, DateTimeStyles.None, out var d2))
        {
            var start = new DateTime(d2.Year, d2.Month, 1);
            return (start, start.AddMonths(1), start.ToString("MMM yyyy", CultureInfo.InvariantCulture));
        }
        throw new InvalidOperationException("INVALID_PERIOD_FORMAT");
    }

    private async Task<int> ResolveInvoiceStatusIdAsync(string code, CancellationToken cancellationToken)
    {
        var id = await db.InvoiceStatuses.AsNoTracking()
            .Where(x => x.IsActive && x.StatusCode == code)
            .Select(x => (int?)x.IdInvoiceStatus)
            .FirstOrDefaultAsync(cancellationToken).ConfigureAwait(false);
        if (id is not { } value) throw new InvalidOperationException($"Invoice status {code} not configured.");
        return value;
    }

    private static string Csv(string? value)
    {
        if (string.IsNullOrEmpty(value)) return "";
        if (!value.Contains(',') && !value.Contains('"') && !value.Contains('\n')) return value;
        return $"\"{value.Replace("\"", "\"\"")}\"";
    }

    private static string BuildReceiptNumber(DateTime nowUtc) => $"RCP-{nowUtc:yyyyMMddHHmmssfff}";
    private static string BuildJvReference(DateTime nowUtc) => $"JV-{nowUtc:yyyyMMddHHmmssfff}";

    private static void ValidateQuickPostRow(QuickPostReceiptRowRequest row, IReadOnlyCollection<string> validModeCodes)
    {
        if (row.InvoiceId <= 0) throw new InvalidOperationException("invoiceId is required.");
        if (row.UnitId <= 0) throw new InvalidOperationException("unitId is required.");
        if (row.PersonId <= 0) throw new InvalidOperationException("personId is required.");
        if (row.PaidDate is null) throw new InvalidOperationException("paidDate is required.");
        if (row.AmountPaid is null || row.AmountPaid <= 0) throw new InvalidOperationException("amountPaid must be > 0.");
        if (string.IsNullOrWhiteSpace(row.PaymentMode)) throw new InvalidOperationException("paymentMode is required.");
        var mode = row.PaymentMode.Trim().ToUpperInvariant();
        if (!validModeCodes.Contains(mode)) throw new InvalidOperationException("INVALID_PAYMENT_MODE");
        if (mode == "CHEQUE")
        {
            if (string.IsNullOrWhiteSpace(row.ChequeNo) || row.ChequeDate is null)
                throw new InvalidOperationException("CHEQUE_DETAILS_MISSING");
        }
        if ((mode == "NEFT" || mode == "RTGS" || mode == "NEFT/RTGS" || mode == "CHEQUE") && row.BankAccountId is null)
            throw new InvalidOperationException("bankAccountId is required for selected payment mode.");
        if (!string.IsNullOrWhiteSpace(row.Remarks) && row.Remarks.Trim().Length > 100)
            throw new InvalidOperationException("remarks can be at most 100 chars.");
    }

    private async Task EnsurePermissionAsync(int apartmentId, PermissionType permission, CancellationToken cancellationToken)
    {
        if (currentUser.IsSuperAdmin) return;
        var roleId = currentUser.ApartmentUserRoleId;
        if (roleId is null) throw new UnauthorizedAccessException("FORBIDDEN");
        var perm = await db.RolePermissions.AsNoTracking()
            .FirstOrDefaultAsync(x => x.ApartmentId == apartmentId && x.RoleId == roleId.Value && x.ModuleCode == ModuleCode, cancellationToken)
            .ConfigureAwait(false);
        if (perm is null) throw new UnauthorizedAccessException("FORBIDDEN");
        var ok = permission switch
        {
            PermissionType.View => perm.CanView,
            PermissionType.Create => perm.CanCreate,
            PermissionType.Edit => perm.CanEdit,
            PermissionType.Delete => perm.CanDelete,
            _ => false
        };
        if (!ok) throw new UnauthorizedAccessException("FORBIDDEN");
    }

    private enum PermissionType { View, Create, Edit, Delete }

