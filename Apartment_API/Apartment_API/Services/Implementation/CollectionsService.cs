    public async Task<CancelReceiptResponseDto> CancelReceiptAsync(
        int apartmentId, int userId, long receiptId, CancelReceiptRequest request, CancellationToken cancellationToken = default)
    {
        await EnsurePermissionAsync(apartmentId, PermissionType.Delete, cancellationToken).ConfigureAwait(false);
        var receipt = await db.PaymentReceipts
            .FirstOrDefaultAsync(x => x.ApartmentId == apartmentId && x.IdPaymentReceipt == receiptId, cancellationToken)
            .ConfigureAwait(false);
        if (receipt == null) throw new InvalidOperationException("RECEIPT_NOT_FOUND");
        if (string.Equals(receipt.Status, "CANCELLED", StringComparison.OrdinalIgnoreCase))
            throw new InvalidOperationException("ALREADY_CANCELLED");

        var allocations = await db.InvoiceReceipts.Where(x => x.PaymentReceiptId == receiptId).ToListAsync(cancellationToken).ConfigureAwait(false);
        var invoiceIds = allocations.Select(x => x.InvoiceId).Distinct().ToList();
        var invoices = await db.Invoices.Where(x => x.ApartmentId == apartmentId && invoiceIds.Contains(x.IdInvoice)).ToListAsync(cancellationToken).ConfigureAwait(false);
        var locked = await db.FiscalYears.AsNoTracking()
            .Where(x => x.ApartmentId == apartmentId && invoices.Select(i => i.FiscalYearId).Contains(x.IdFiscalYear) && x.IsLocked)
            .AnyAsync(cancellationToken).ConfigureAwait(false);
        if (locked) throw new InvalidOperationException("PERIOD_LOCKED");

        var statusUnpaidId = await ResolveInvoiceStatusIdAsync("UNPAID", cancellationToken).ConfigureAwait(false);
        var statusPartialId = await ResolveInvoiceStatusIdAsync("PARTIAL", cancellationToken).ConfigureAwait(false);

        await using var tx = await db.Database.BeginTransactionAsync(cancellationToken).ConfigureAwait(false);
        foreach (var allocation in allocations)
        {
            var invoice = invoices.First(x => x.IdInvoice == allocation.InvoiceId);
            invoice.PaidAmount = Math.Max(0m, invoice.PaidAmount - allocation.AllocatedAmount);
            invoice.BalanceAmount = Math.Max(0m, invoice.TotalAmount - invoice.PaidAmount);
            invoice.StatusId = invoice.PaidAmount <= 0 ? statusUnpaidId : statusPartialId;
            invoice.UpdatedAt = DateTime.UtcNow;
            invoice.UpdatedBy = userId;
        }

        db.InvoiceReceipts.RemoveRange(allocations);
        receipt.Status = "CANCELLED";
        receipt.Narration = string.IsNullOrWhiteSpace(request.CancelReason)
            ? receipt.Narration
            : $"Cancelled: {request.CancelReason.Trim()}";
        receipt.UpdatedAt = DateTime.UtcNow;
        receipt.UpdatedBy = userId;
        await db.SaveChangesAsync(cancellationToken).ConfigureAwait(false);
        await tx.CommitAsync(cancellationToken).ConfigureAwait(false);

        return new CancelReceiptResponseDto
        {
            ReceiptId = receipt.IdPaymentReceipt,
            ReceiptNumber = receipt.ReceiptNumber,
            Cancelled = true,
            ReversalJvReference = $"JV-REV-{DateTime.UtcNow:yyyyMMddHHmmss}",
            Message = "Receipt cancelled and reversal JV posted successfully."
        };
    }

