    public async Task<CollectionsSummaryDto> GetSummaryAsync(int apartmentId, string? period, CancellationToken cancellationToken = default)
    {
        await EnsurePermissionAsync(apartmentId, PermissionType.View, cancellationToken).ConfigureAwait(false);
        var (start, end, label) = ResolvePeriod(period);
        var invoices = await db.Invoices.AsNoTracking()
            .Where(x => x.ApartmentId == apartmentId && x.InvoiceDate >= start && x.InvoiceDate < end)
            .Select(x => new { x.TotalAmount, x.PaidAmount, x.BalanceAmount })
            .ToListAsync(cancellationToken)
            .ConfigureAwait(false);

        var billed = invoices.Sum(x => x.TotalAmount);
        var collected = invoices.Sum(x => x.PaidAmount);
        var outstanding = invoices.Sum(x => x.BalanceAmount ?? Math.Max(0m, x.TotalAmount - x.PaidAmount));
        var rate = billed <= 0 ? 0 : Math.Round((collected / billed) * 100m, 2);
        return new CollectionsSummaryDto
        {
            BilledAmount = billed,
            CollectedAmount = collected,
            OutstandingAmount = outstanding,
            CollectionRatePercent = rate,
            Period = label
        };
    }

    public async Task<PagedResult<CollectionInvoiceListItemDto>> ListInvoicesAsync(
        int apartmentId, string? search, string? period, int? incomeHeadId, int? statusId, int pageNumber, int pageSize, CancellationToken cancellationToken = default)
    {
        await EnsurePermissionAsync(apartmentId, PermissionType.View, cancellationToken).ConfigureAwait(false);
        pageNumber = Math.Max(1, pageNumber);
        pageSize = Math.Clamp(pageSize, 1, 100);
        var q = BaseInvoiceQuery(apartmentId);
        if (!string.IsNullOrWhiteSpace(search))
        {
            var s = search.Trim();
            q = q.Where(x => (x.i.InvoiceNumber ?? string.Empty).Contains(s) || x.u.UnitNumber.Contains(s) || x.p.FullName.Contains(s));
        }
        if (incomeHeadId is { } ihid) q = q.Where(x => x.i.IncomeHeadId == ihid);
        if (statusId is { } sid) q = q.Where(x => x.i.StatusId == sid);
        if (!string.IsNullOrWhiteSpace(period))
        {
            if (!DateTime.TryParseExact(period.Trim() + "-01", "yyyy-MM-dd", CultureInfo.InvariantCulture, DateTimeStyles.None, out var start))
                throw new InvalidOperationException("INVALID_PERIOD_FORMAT");
            var end = start.AddMonths(1);
            q = q.Where(x => x.i.InvoiceDate >= start && x.i.InvoiceDate < end);
        }

        var total = await q.CountAsync(cancellationToken).ConfigureAwait(false);
        var rows = await q.OrderByDescending(x => x.i.InvoiceDate).ThenByDescending(x => x.i.IdInvoice)
            .Skip((pageNumber - 1) * pageSize).Take(pageSize).ToListAsync(cancellationToken).ConfigureAwait(false);

        var items = rows.Select(x => new CollectionInvoiceListItemDto
        {
            InvoiceId = x.i.IdInvoice,
            InvoiceNumber = x.i.InvoiceNumber ?? string.Empty,
            UnitId = x.i.UnitId,
            UnitNumber = x.u.UnitNumber,
            PersonId = x.i.PersonId,
            OwnerName = x.p.FullName,
            IncomeHeadId = x.i.IncomeHeadId,
            IncomeHeadName = x.h.HeadName,
            InvoiceDate = x.i.InvoiceDate,
            DueDate = x.i.DueDate,
            Amount = x.i.Amount,
            GstAmount = x.i.GstAmount,
            TotalAmount = x.i.TotalAmount,
            PaidAmount = x.i.PaidAmount,
            BalanceAmount = x.i.BalanceAmount ?? Math.Max(0m, x.i.TotalAmount - x.i.PaidAmount),
            StatusId = x.i.StatusId,
            StatusName = x.s.StatusName,
            StatusCode = x.s.StatusCode,
            Period = x.i.InvoiceDate.ToString("MMM yyyy", CultureInfo.InvariantCulture),
            IsAutoGenerated = x.i.IsAutoGenerated
        }).ToList();

        return new PagedResult<CollectionInvoiceListItemDto>
        {
            Items = items,
            TotalCount = total,
            Page = pageNumber,
            PageSize = pageSize
        };
    }

